package ex1;

public class Calcul {

	/** Cette methode effectue une addition
	 * 
	 * Methods
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public int additioner(int a, int b){
		return a+b;
	}
	
	/** Cette methode effectue une soustraction
	 * Methods
	 * @param a
	 * @param b
	 * @return a-b
	 */
	public int soustraire(int a, int b){
		return a-b;
	}
}
